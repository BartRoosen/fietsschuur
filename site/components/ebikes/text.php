    <h2>Van je fiets een e-bike maken...</h2><hr>
    <p>
        Wil je profiteren van de voordelen van een elektrische fiets, maar je huidige fiets wil je niet weg doen.
        Laat dan je fiets ombouwen tot e-bike in <strong>'t Fietsschurreke</strong>.
    </p>
    <p>
        Het ombouwen van je fiets verloopt volgens deze stappen:
    </p>
    <ul>
        <li>Bel <strong>&#48;&#52;&#57;&#55;/&#48;6.4&#55;.0&#51;</strong> voor een afspraak</li>
        <li>Breng je fiets mee op de afspraak zodat deze kan opgemeten worden en een offerte kan opgesteld worden</li>
        <li>Zodra U akkoord gaat met de offerte kunnen de onderdelen besteld worden</li>
        <li>Eens onderdelen aangekomen zijn krijg je een seintje</li>
        <li>Breng je fiets binnen zodat hij omgebouwd kan worden</li>
        <li>Haal je fiets op en geniet van de voordelen van een elektrische fiets</li>
    </ul>
